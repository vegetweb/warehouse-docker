/**
 * Create a menu item for the log browser.
 */
function log_browser_alter_menu($menu, $auth) {
  if ($auth->has_any_website_access('admin') || $auth->logged_in('CoreAdmin'))
    $menu['Admin']['Browse server logs']='browse_server_logs';
  return $menu;
}